// Função para adicionar os dados de exemplo ao Firebase
export const addSampleDataToFirebase = async () => {
  const { addVadeMecumDocument } = await import('../services/vadeMecumService')
  
  try {
    const results = []
    for (const document of sampleVadeMecumData) {
      const result = await addVadeMecumDocument(document)
      results.push(result)
    }
    
    const successCount = results.filter(r => r.success).length
    console.log(`${successCount} documentos adicionados com sucesso ao Vade Mecum`)
    
    return { success: true, count: successCount }
  } catch (error) {
    console.error('Erro ao adicionar dados de exemplo:', error)
    return { success: false, error: error.message }
  }
}

